        jTextArea1.append("\n--------------------------------------------------------");
        jTextArea1.append("\nNúmero de classes: "+numberOfClasses.getNumberOfClasses());
        jTextArea1.append("\nNúmero de interfaces: "+numberOfInterfaces.getNumberOfInterface());
        jTextArea1.append("\nDit Máximo: "+maxDit.getMaxDit());
